--FactLoanContract
CREATE OR ALTER VIEW VW_FactLoanContract 
AS
SELECT th.MA_HOPDONG_TINDUNG,
       kh.MA_KHACHHANG,
       kh.MA_CHINHANH,
       kh.MA_NGANHNGHE,
       ISNULL(hdtd.SOTIEN_GIAINGAN,0)AS SOTIEN_GIAINGAN,
       ISNULL(th.TRA_GOC,0 ) AS TRA_GOC,
       ISNULL(th.TRA_LAI,0) AS TRA_LAI,
       
       EOMONTH(th.NGAY_TRA) AS NGAYTRACUOITHANG
FROM TBL_THUHOINO th 
LEFT JOIN TBL_HOPDONG_TINDUNG hdtd ON hdtd.MA_HOPDONG_TINDUNG = th.MA_HOPDONG_TINDUNG
LEFT JOIN TBL_KHACH_HANG kh ON kh.MA_KHACHHANG = hdtd.MA_KHACHHANG

-- FactCollateral
CREATE OR ALTER VIEW VW_FactCollateral
AS
SELECT ts.MA_HOPDONG_TINDUNG,
       kh.MA_KHACHHANG,
       kh.MA_CHINHANH,
       MA_TP,
      
       ISNULL(ts.GIATRI_TAISAN,0) AS GIATRI_TAISAN
FROM TBL_TAISAN_BAODAM ts
LEFT JOIN TBL_HOPDONG_TINDUNG hdtd ON hdtd.MA_HOPDONG_TINDUNG = ts.MA_HOPDONG_TINDUNG
LEFT JOIN TBL_KHACH_HANG kh ON kh.MA_KHACHHANG = hdtd.MA_KHACHHANG
--FactCustomerBranch
CREATE OR ALTER VIEW VW_FactCustomerBranch
AS
SELECT TOP 10 cn.MA_CHINHANH,
       COUNT(DISTINCT MA_KHACHHANG) AS SOKHACHHANG
FROM TBL_KHACH_HANG kh
LEFT JOIN TBL_CHINHANH cn ON kh.MA_CHINHANH = cn.MA_CHINHANH
GROUP BY cn.MA_CHINHANH
ORDER BY SOKHACHHANG DESC