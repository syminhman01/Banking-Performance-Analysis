--FactCurrentAccountTransaction
CREATE OR ALTER VIEW VW_FactCurrentAccountTransaction AS
SELECT
    kh.MA_KHACHHANG,
    kh.MA_CHINHANH,

    CAST(tgtt.THOIGIAN_GIAODICH AS DATE) AS TransactionDate,
    EOMONTH (tgtt.THOIGIAN_GIAODICH) AS NGAYCUOITHANG,
    ISNULL(tgtt.SOTIEN_TANG, 0) AS InflowAmount,
    ISNULL(tgtt.SOTIEN_GIAM, 0) AS OutflowAmount,
    ISNULL(tgtt.SOTIEN_TANG, 0) - ISNULL(tgtt.SOTIEN_GIAM, 0) AS NetAmount

FROM TBL_TIENGUI_THANHTOAN tgtt
LEFT JOIN TBL_KHACH_HANG kh
    ON tgtt.MA_TAIKHOAN_THANHTOAN = kh.MA_TAIKHOAN_THANHTOAN

--FactSavingsDeposit
CREATE OR ALTER VIEW VW_FactSavingsDeposit 
AS
SELECT kh.MA_KHACHHANG,  
       kh.MA_CHINHANH,
       kh.MA_NGANHNGHE,
       tk.MA_TKTK,
       tk.LAI_SUAT*tk.KYHAN*tk.SOTIEN_GUI/100 AS LAIPHAITRA,
       tk.TATTOAN,
       CASE 
           WHEN tk.KYHAN<=1 THEN N'Không kỳ hạn / 1 tháng'
           WHEN tk.KYHAN<=3 THEN N'Ngắn hạn'
           WHEN tk.KYHAN<=6 THEN N'Trung hạn'
           WHEN tk.KYHAN<=12 THEN N'Dài hạn'
           ELSE N'Trên 12 tháng'
       END AS "Nhóm kỳ hạn",
       EOMONTH (tk.NGAY_GUI) AS NGAYGUICUOITHANG,
       EOMONTH(tk.NGAY_DAOHAN) AS NGAYDAOHANCUOITHANG,
       ISNULL(tk.SOTIEN_GUI,0) AS SOTIEN_GUI

FROM TBL_TIENGUI_TIETKIEM tk
LEFT JOIN TBL_KHACH_HANG kh ON kh.MA_KHACHHANG = tk.MA_KHACHHANG

